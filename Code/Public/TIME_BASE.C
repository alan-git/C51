#include "TIME_BASE.H"

/* *******************************************************************************************
// 时基时钟初始化
/* ******************************************************************************************* */
// 利用STC-ISP软件导出此函数：
static void Timer0Init(void)    //2毫秒@24.000MHz
{
    AUXR |= 0x80;   //定时器时钟1T模式
    TMOD &= 0xF0;   //设置定时器模式
    TL0 = 0x80;     //设置定时初值
    TH0 = 0x44;     //设置定时初值
    TF0 = 0;        //清除TF0标志
    TR0 = 1;        //定时器0开始计时
}

// 时基定时器初始化并开启中断总开关：
void InitInterrupt()
{
#ifdef T_0
    Timer0Init();
    ET0 = 1;
#elif T_1
    Timer1Init();
    ET1 = 1;
#elif T_2
    Timer2Init();
    IE2 |= 0x04;
#endif
    EA = 1;
}

